/*
Take three int variables.
Declare first and assign values later, then print them.
*/
#include<stdio.h>
void main()
{
    int a, b, c; // variables declared

    a = 10;      // value assigned to a
    b = 10;      // value assigned to b
    c = 10;      // value assigned to c

    printf("%d\n", a); // prints a
    printf("%d\n", b); // prints b
    printf("%d\n", c); // prints c
}

/*
Output:
10
10
10

Reason:
Variables are declared before use and assigned values correctly.
*/
