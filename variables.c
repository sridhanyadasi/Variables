/*
Take three int variables.
Initialize each variable separately and print them.
*/
#include<stdio.h>
void main()
{
    int a = 10;   // a initialized with 10
    int b = 10;   // b initialized with 10
    int c = 10;   // c initialized with 10

    printf("%d\n", a); // prints a
    printf("%d\n", b); // prints b
    printf("%d\n", c); // prints c
}

/*
Output:
10
10
10

Reason:
Each variable is declared and initialized separately, so no error occurs.
*/
