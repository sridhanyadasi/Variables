/*
Take three int variables.
Declare and initialize them in a single line and print values.
*/
#include<stdio.h>
void main()
{
    int a = 10, b = 10, c = 10; // multiple variables in one statement

    printf("%d\n", a); // prints a
    printf("%d\n", b); // prints b
    printf("%d\n", c); // prints c
}

/*
Output:
10
10
10

Reason:
Same datatype can be used for multiple variables in a single line.
*/
